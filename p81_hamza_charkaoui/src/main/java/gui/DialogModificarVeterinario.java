    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {

            String nif = txtNif.getText();
            String nombre = txtNombre.getText().isEmpty() ? null : txtEmail.getText();
            String direccion = txtDireccion.getText().isEmpty() ? null : txtEmail.getText();         
            String telefono = txtTelefono.getText().isEmpty() ? null : txtTelefono.getText();
            String email = txtEmail.getText().isEmpty() ? null : txtEmail.getText();

            if (nif.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El NIF es obligatorio");
            } else {
                VeterinarioDTO nuevo = new VeterinarioDTO(
                        idVeterinario, nif, nombre, direccion, telefono, email);

                VeterinarioDAO dao = new VeterinarioDAO();

                dao.updateVeterinario(idVeterinario, nuevo);

                JOptionPane.showMessageDialog(this, "Modificado correctamente");
                // goes back
                this.dispose();

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this, "Error al modificar");
            e.printStackTrace();
        }

    }// GEN-LAST:event_btnGuardarActionPerformed

