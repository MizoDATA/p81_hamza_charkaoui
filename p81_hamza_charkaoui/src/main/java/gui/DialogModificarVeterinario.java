    private void cargarDatos() {

        try {

            // crear el DAO para acceder a la base de datos
            VeterinarioDAO dao = new VeterinarioDAO();

            // buscar el veterinario por su ID (que ya guardamos antes)
            VeterinarioDTO vet = dao.findByPk(idVeterinario);

            // comprobar que el veterinario existe
            if (vet != null) {

                // poner el ID en el campo de texto
                txtId.setText(String.valueOf(vet.getId_veterinario()));
                // hacer que el ID no se pueda modificar
                txtId.setEditable(false);
                // poner el nif en el campo
                txtNif.setText(vet.getNif());
                // poner el nombre
                txtNombre.setText(vet.getNomvete());
                // poner la dirección
                txtDireccion.setText(vet.getDir());
                // poner el teléfono
                txtTelefono.setText(vet.getTelefono());
                // poner el email
                txtEmail.setText(vet.getEmail());
            }

        } catch (SQLException e) {

            // mostrar mensaje si hay error con la base de datos
            JOptionPane.showMessageDialog(this, "Error al cargar datos");

            // imprimir error en consola (para depurar)
            e.printStackTrace();
        }
    }

