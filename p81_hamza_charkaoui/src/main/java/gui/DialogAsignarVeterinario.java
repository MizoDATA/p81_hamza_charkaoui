    private void cargarMascotas() {

        try {

            MascotaDAO dao = new MascotaDAO();
            // lista de mascotas
            List<MascotaDTO> lista = dao.getAll();

            // vaciar el combobox antes de meter cosas , flow nextline()
            comboMascotas.removeAllItems();

            for (MascotaDTO m : lista) {
                // convertir id a String
                comboMascotas.addItem(String.valueOf(m.getId_mascota()));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void cargarVeterinarios() {

        try {

            VeterinarioDAO dao = new VeterinarioDAO();
            // lista de veterinarios
            List<VeterinarioDTO> lista = dao.getAll();

            comboVeterinarios.removeAllItems();

            for (VeterinarioDTO v : lista) {
                // convertir id a String
                comboVeterinarios.addItem(String.valueOf(v.getId_veterinario()));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

