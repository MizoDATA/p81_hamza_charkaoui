    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        // TODO add your handling code here:
        try {

            // coger String del combo
            String idMascotaStr = (String) comboMascotas.getSelectedItem();
            String idVetStr = (String) comboVeterinarios.getSelectedItem();

            if (idMascotaStr == null || idVetStr == null) {
                JOptionPane.showMessageDialog(this, "Selecciona datos");
                return;
            }

            // convertir a int
            int idMascota = Integer.parseInt(idMascotaStr);
            int idVeterinario = Integer.parseInt(idVetStr);

            MascotaDAO dao = new MascotaDAO();

            // buscar mascota
            MascotaDTO mascota = dao.findByPk(idMascota);

            if (mascota == null) {
                JOptionPane.showMessageDialog(this, "Mascota no existe");
                return;
            }

            // asignar veterinario
            mascota.setId_veterinario(idVeterinario);

            // actualizar
            dao.updateMascota(idMascota, mascota);

            JOptionPane.showMessageDialog(this, "Asignado correctamente");

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this, "Error");

            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAsignarActionPerformed

